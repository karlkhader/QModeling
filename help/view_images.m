%% View Parametric Images
%% How can I see the parametric images obtained?
% Once you have calculated these images, the _View Parametric Images_
% panel will be enabled. Select the image from the drop-down list
% and press the _View Image_ button. The image will be shown in a new
% window. 
%
%% Can I see more than one image at the same time?
% Yes. Each time you push the _View Image_ button, the image selected from
% the drop-down list will be displayed in a new window.
%
%% Can I visualize images stored in my computer?
% Yes. The option _View Image_ is shown on the _QModeling_ tab. Select it
% to view any NIfTI or Analyze 3D image you have on your computer.
%